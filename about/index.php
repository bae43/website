					<br />
					<br />

					Recently my interests have shifted towards distributed web development,
					primarily websockets and their ability to support fast real-time multi-user applications.
					With my ongoing project <a href="../projects/featured/index.php">Campus Conquest</a>, I've been
					exploring both the use of sockets for a fast multiplayer experience and the power of  WebGL to render 3D graphics in a browser.
					Together these relatively new technologies allow running a massive multiplayer game with high end graphics
					that doesn't even require a download, just instantaneous access from anywhere. Undertaking Campus Conquest
					and learning about these topics has been very fun and rewarding.

